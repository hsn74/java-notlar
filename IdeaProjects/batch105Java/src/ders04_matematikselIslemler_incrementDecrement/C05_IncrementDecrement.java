package ders04_matematikselIslemler_incrementDecrement;

public class C05_IncrementDecrement {
    public static void main(String[] args) {
        int sayi=10;
        sayi=sayi+3;
        System.out.println(sayi);

        int s=10;
        s=s+1;
        System.out.println(s);
        s++;
        System.out.println(s);
        int t=10;
        --t;
        System.out.println(t);
    }
}
