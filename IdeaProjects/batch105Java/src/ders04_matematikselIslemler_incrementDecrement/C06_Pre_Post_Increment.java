public class C06_Pre_Post_Increment {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        a++;
        int c=10;
        System.out.println("c:"+c++);
        int d=10;
        System.out.println("d:"+ ++d);
    }
}
